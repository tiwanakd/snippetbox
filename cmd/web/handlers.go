func (app *application) accountView(w http.ResponseWriter, r *http.Request) {
	id := app.sessionManager.GetInt(r.Context(), "authenticatedUserID")

	user, err := app.users.Get(id)
	if err != nil {
		if errors.Is(err, models.ErrNoRecord) {
			http.Redirect(w, r, "/user/login", http.StatusSeeOther)
		} else {
			app.serverError(w, r, err)
		}
		return
	}
	data := app.newTemplateData(r)
	data.User = user
	app.render(w, r, http.StatusOK, "account.tmpl", data)
}

type passwordChangeForm struct {
	CurrentPassword     string `form:"currentPassword"`
	NewPassword         string `form:"newPassword"`
	ConfirmPassword     string `form:"confirmPassword"`
	validator.Validator `form:"-"`
}

func (app *application) changePassword(w http.ResponseWriter, r *http.Request) {
	data := app.newTemplateData(r)
	data.Form = passwordChangeForm{}
	app.render(w, r, http.StatusOK, "password.tmpl", data)
}

func (app *application) changePasswordPost(w http.ResponseWriter, r *http.Request) {
	var form passwordChangeForm
	err := app.decodePostForm(r, &form)
	if err != nil {
		app.clientError(w, http.StatusBadRequest)
		return
	}

	form.CheckField(validator.NotBlank(form.CurrentPassword), "currentPassword", "This field cannot be empty")
	form.CheckField(validator.NotBlank(form.NewPassword), "newPassword", "This field cannot be empty")
	form.CheckField(validator.MinChars(form.NewPassword, 8), "newPassword", "Password should be atleast 8 Characters long")
	form.CheckField(validator.NotBlank(form.ConfirmPassword), "confirmPassword", "This field cannot be empty")

	if form.NewPassword != form.ConfirmPassword {
		form.AddFieldError("newPassword", "Passwords do not Match")
		form.AddFieldError("confirmPassword", "Passwords do not Match")
	}

	if !form.Valid() {
		data := app.newTemplateData(r)
		data.Form = form
		app.render(w, r, http.StatusUnprocessableEntity, "password.tmpl", data)
		return
	}

	id := app.sessionManager.GetInt(r.Context(), "authenticatedUserID")
	err = app.users.PasswordUpdate(id, form.CurrentPassword, form.NewPassword)
	if err != nil {
		if errors.Is(err, models.ErrInvalidCredentials) {
			form.AddFieldError("currentPassword", "Invalid Current Password")
			data := app.newTemplateData(r)
			data.Form = form
			app.render(w, r, http.StatusUnprocessableEntity, "password.tmpl", data)
		} else if errors.Is(err, models.ErrDuplictePasswordUpdate) {
			form.AddFieldError("newPassword", "New Password cannot be same as Current Password")
			data := app.newTemplateData(r)
			data.Form = form
			app.render(w, r, http.StatusUnprocessableEntity, "password.tmpl", data)
		} else {
			app.serverError(w, r, err)
		}
		return
	}

	err = app.sessionManager.RenewToken(r.Context())
	if err != nil {
		app.serverError(w, r, err)
		return
	}

	app.sessionManager.Put(r.Context(), "flash", "Your Password has been updated!")
	http.Redirect(w, r, "/account/view", http.StatusSeeOther)
}
